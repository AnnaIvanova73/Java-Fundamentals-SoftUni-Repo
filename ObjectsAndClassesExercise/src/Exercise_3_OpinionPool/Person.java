package Exercise_3_OpinionPool;

public class Person {
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public int getAge() {
        return this.age;
    }

    @Override
    public String toString() {
        String result = String.format("%s - %d", this.name, this.age);
        return result;
    }


}
